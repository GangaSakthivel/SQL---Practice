sql
Copy
SELECT o.OrderID, b.Title, o.Quantity, o.OrderDate
FROM Orders o
JOIN Books b ON o.BookID = b.BookID
WHERE YEAR(o.OrderDate) = 2023;
Find the average price of books in each genre.

sql
Copy
SELECT Genre, AVG(Price) AS AveragePrice
FROM Books
GROUP BY Genre;
