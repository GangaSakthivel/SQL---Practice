Retrieve all books written by George Orwell.

sql
Copy
SELECT b.Title
FROM Books b
JOIN Authors a ON b.AuthorID = a.AuthorID
WHERE a.AuthorName = 'George Orwell';
Find the total revenue generated from all orders.

sql
Copy
SELECT SUM(b.Price * o.Quantity) AS TotalRevenue
FROM Orders o
JOIN Books b ON o.BookID = b.BookID;
List all books published before 1950.

sql
Copy
SELECT Title
FROM Books
