Find the total number of books sold for each genre.

sql
Copy
SELECT b.Genre, SUM(o.Quantity) AS TotalSold
FROM Orders o
JOIN Books b ON o.BookID = b.BookID
GROUP BY b.Genre;
Retrieve the names of authors who have written more than one book.

sql
Copy
SELECT a.AuthorName
FROM Authors a
JOIN Books b ON a.AuthorID = b.AuthorID
GROUP BY a.AuthorName
HAVING COUNT(b.BookID) > 1;
Find the most expensive book in the database.

sql
Copy
SELECT Title, Price
FROM Books
ORDER BY Price DESC
LIMIT 1;
