Find the book with the highest number of orders.

sql
Copy
SELECT b.Title, SUM(o.Quantity) AS TotalOrders
FROM Orders o
JOIN Books b ON o.BookID = b.BookID
GROUP BY b.Title
ORDER BY TotalOrders DESC
LIMIT 1;
